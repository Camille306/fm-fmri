import os
import warnings
import numpy as np
from pathlib import Path
from typing import Dict, List, Optional, Tuple
import torch

# EV folder names under extracted_txt/EVs/ (HCP tfMRI naming). Keys: dataset task_name (lowercase); values: folder name on disk.
# Structure: ev_root/extracted_txt/EVs/{ev_folder_name}/{subject_id}/. Only .txt files are loaded (no CSV).
# Every subject has Sync.txt (one number); other .txt files (e.g. fear.txt, neut.txt) have multiple columns.
TASK_TO_EV_FOLDER: Dict[str, str] = {
    "emotion": "EMOTION",
    "gambling": "GAMBLING",
    "language": "LANGUAGE",
    "motor": "MOTOR",
    "relational": "RELATIONAL",
    "social": "SOCIAL",
    "WM": "WM",
}

# Task -> condition names (1=first, 2=second, ...). Generated by build_ev_condition_mappings.py.
TASK_CONDITION_MAP: Dict[str, List[str]] = {}
try:
    _ev_mapping_path = Path(__file__).resolve().parent / "ev_condition_mappings.py"
    if _ev_mapping_path.exists():
        import importlib.util
        _spec = importlib.util.spec_from_file_location("ev_condition_mappings", _ev_mapping_path)
        _mod = importlib.util.module_from_spec(_spec)
        _spec.loader.exec_module(_mod)
        TASK_CONDITION_MAP = getattr(_mod, "TASK_CONDITION_MAP", {})
except Exception:
    pass


class HCPRestingFCDataset:
    """
    Dataset loader for HCP resting-state + task timeseries data.
    
    Loads resting-state timeseries files from subject folders in the HCP resting FC directory.
    This repo is currently configured to use **AAL3** resting-state timeseries:
    - `REST1_LR_AAL3_ts.npy`
    so that the ROI dimension matches the task data parcellation.
    """
    
    def __init__(
        self,
        data_root: str = "./data/hcp-resting-fc",
        task_root: Optional[str] = None,
        task_name: str = "emotion",
        use_evs: bool = False,
        ev_root: Optional[str] = None,
    ):
        """
        Initialize the dataset.

        Args:
            data_root: Root directory containing subject folders for resting state data
            task_root: (Deprecated/ignored) Task data now loads from ./data/palmer_scratch/{task}/{sub_id}
            task_name: Name of the task (e.g., "emotion", "gambling", "language", "motor", "relational", "social", "WM")
                      Must be one of: emotion, gambling, hcp_rest, language, motor, relational, social, WM
            use_evs: If True, require and load EV data from extracted_txt/EVs per subject.
            ev_root: Base path for EV files. Under ev_root: extracted_txt/EVs/{task_folder}/{subject_id}/.
                     If None: uses .//rest_to_task when that path exists (HCP), else cwd (local testing).
                     Pass a path (e.g. repo root) for explicit local testing.
        """
        self.data_root = Path(data_root)
        self.task_root = Path(task_root) if task_root else None
        self.task_name = task_name
        self.use_evs = use_evs
        # EV root: use HCP path on cluster if it exists; otherwise fall back to cwd for local testing.
        if ev_root:
            self.ev_root = Path(ev_root)
        else:
            _hcp_default = Path(".//rest_to_task")
            self.ev_root = _hcp_default if _hcp_default.exists() else Path.cwd()

        # When use_evs=True: population = ALL subjects that have rest+task data (same as use_evs=False).
        # Subjects that additionally have EVs will receive EV conditioning; those without EVs
        # receive a zero EV tensor + zero mask (handled in FMRIWindowDataset.__getitem__).
        # This avoids shrinking the training set when EV extraction is incomplete.
        if self.task_name and use_evs:
            self.subject_ids = self._get_subject_ids()
            self.subject_ids = self._filter_paired_subjects(self.subject_ids)
            _n_all = len(self.subject_ids)
            _n_ev  = sum(1 for s in self.subject_ids if self._has_ev(s))
            print(
                f"[HCPRestingFCDataset] use_evs=True: {_n_all} subjects with rest+task; "
                f"{_n_ev} have EV files ({_n_all - _n_ev} will train with zero EV)."
            )
        elif self.task_name:
            self.subject_ids = self._get_subject_ids()
            self.subject_ids = self._filter_paired_subjects(self.subject_ids)
        else:
            self.subject_ids = self._get_subject_ids()

    def _get_subject_ids(self) -> list:
        """Get all subject IDs from the data root directory."""
        if not self.data_root.exists():
            raise ValueError(f"Data root directory does not exist: {self.data_root}")
        subject_ids = []
        for item in self.data_root.iterdir():
            if item.is_dir():
                subject_ids.append(item.name)
        return sorted(subject_ids)

    def _get_ev_folder_name(self) -> str:
        """Return the EV folder name for the current task (e.g. EMOTION, WM). Handles different tasks via TASK_TO_EV_FOLDER."""
        key = (self.task_name or "").strip()
        return TASK_TO_EV_FOLDER.get(key, key.upper())

    def _get_subject_ids_from_evs(self) -> list:
        """
        Get subject IDs that have EV data under extracted_txt/EVs/{task_folder}/{subject_id}.
        Task folder is resolved via TASK_TO_EV_FOLDER so each task (emotion, gambling, ...) maps to the correct folder.
        """
        task_folder = self._get_ev_folder_name()
        task_ev_base = self.ev_root / "extracted_txt" / "EVs" / task_folder
        if not task_ev_base.exists():
            return []
        exts = (".txt",)
        subject_ids = []
        for item in task_ev_base.iterdir():
            if not item.is_dir():
                continue
            ev_dir = item
            has_sync = (ev_dir / "Sync.txt").exists() or (ev_dir / "sync.txt").exists()
            has_txt = any(f.is_file() and f.suffix.lower() in exts for f in ev_dir.iterdir())
            if has_sync and has_txt:
                subject_ids.append(item.name)
        return sorted(subject_ids)

    def _has_ev(self, subject_id: str) -> bool:
        """Return True if this subject has a non-empty EV directory with at least one .txt file."""
        ev_dir = self.get_ev_dir(subject_id)
        if not ev_dir.exists() or not ev_dir.is_dir():
            return False
        has_sync = (ev_dir / "Sync.txt").exists() or (ev_dir / "sync.txt").exists()
        has_txt  = any(f.is_file() and f.suffix.lower() == ".txt" for f in ev_dir.iterdir())
        return has_sync and has_txt

    def _filter_to_rest_and_task(self, subject_ids: list) -> list:
        """Keep only subjects that have both resting and task data (for the EV population)."""
        paired = []
        for subject_id in subject_ids:
            if self.get_subject_path(subject_id).exists() and self.get_task_path(subject_id).exists():
                paired.append(subject_id)
        return sorted(paired)

    def _filter_paired_subjects(self, subject_ids: list) -> list:
        """Filter to only subjects that have both resting and task data."""
        paired = []
        for subject_id in subject_ids:
            if self.get_subject_path(subject_id).exists() and self.get_task_path(subject_id).exists():
                paired.append(subject_id)
        return sorted(paired)

    def get_ev_dir(self, subject_id: str) -> Path:
        """
        Get the path to a subject's EVs directory.
        Path structure: ev_root/extracted_txt/EVs/{task_folder}/{subject_id}
        (e.g. .//rest_to_task/extracted_txt/EVs/EMOTION/100206). Task folder from TASK_TO_EV_FOLDER.
        """
        task_folder = self._get_ev_folder_name()
        return self.ev_root / "extracted_txt" / "EVs" / task_folder / subject_id

    def get_subject_path(self, subject_id: str) -> Path:
        """
        Get the path to a subject's resting state timeseries file.
        
        Args:
            subject_id: Subject ID string
            
        Returns:
            Path to resting-state timeseries file (currently `REST1_LR_AAL3_ts.npy`)
        """
        # Use AAL3 parcellation to match task ROI dimensionality.
        return self.data_root / subject_id / "timeseries" / "REST1_LR_AAL3_ts.npy"
    
    def get_task_path(self, subject_id: str) -> Path:
        """
        Get the path to a subject's task timeseries file.
        
        Args:
            subject_id: Subject ID string
            
        Returns:
            Path to task timeseries file
            
        Path structure: ./data/palmer_scratch/{task}/{sub_id}/{sub_id}_AAL3_ts.npy
        """
        return Path("./data/palmer_scratch") / self.task_name / subject_id / f"{subject_id}_AAL3_ts.npy"

    def _ev_extensions(self) -> tuple:
        """File extensions to load as EV files. Only .txt is loaded (CSV and .1D are not read)."""
        return (".txt",)

    def _ev_files_in_dir(self, ev_dir: Path) -> List[Path]:
        """
        Return list of EV .txt files to load (sorted for reproducibility).
        Sync.txt is handled separately as a single scalar; other .txt files are loaded as (T, n_cols).
        """
        exts = self._ev_extensions()
        return sorted([f for f in ev_dir.iterdir() if f.is_file() and f.suffix.lower() in exts])

    def load_ev_subject(self, subject_id: str) -> np.ndarray:
        """
        Load EV (explanatory variable) data from extracted_txt/EVs for a subject.
        Condition .txt files (e.g. fear.txt, neut.txt) are loaded in full (onset, duration, amplitude).
        An extra column codes the condition: first file = 1, second = 2, etc. Rows are vertically
        concatenated (fear rows then neut rows). Sync.txt is not included (same value for all rows).

        Args:
            subject_id: Subject ID string

        Returns:
            numpy array (N, C+1): N = total rows; columns 0..C-1 = read content (onset, duration, amplitude);
            column C = condition code (1, 2, ...). Normalized: col0 and col1 divided by (177*0.72),
            col2 z-scored (mean 0, std 1), condition column unchanged.
        """
        if not self.task_name:
            raise ValueError("Task name not specified for EV loading")
        ev_dir = self.get_ev_dir(subject_id)
        if not ev_dir.exists() or not ev_dir.is_dir():
            raise FileNotFoundError(
                f"EVs directory not found for subject {subject_id}: {ev_dir}"
            )
        files = self._ev_files_in_dir(ev_dir)
        sync_path = next((f for f in files if f.name.lower() == "sync.txt"), None)
        if sync_path is None:
            raise FileNotFoundError(
                f"Sync.txt not found in {ev_dir} for subject {subject_id} (every subject must have Sync.txt)"
            )

        other_files = [f for f in files if f.name.lower() != "sync.txt"]
        # Order by TASK_CONDITION_MAP when available so condition codes match mapping (1=first name, etc.)
        order = TASK_CONDITION_MAP.get((self.task_name or "").strip())
        if order:
            by_stem = {f.stem.lower(): f for f in other_files}
            ordered = [by_stem[s] for s in order if s in by_stem]
            extra = sorted([f for f in other_files if f.stem.lower() not in {s.lower() for s in order}])
            other_files = ordered + extra
        else:
            other_files = sorted(other_files)

        blocks = []
        for cond_code, f in enumerate(other_files, start=1):
            try:
                with warnings.catch_warnings():
                    warnings.simplefilter("ignore")
                    arr = np.loadtxt(str(f), dtype=np.float64, ndmin=2)
                if arr.ndim == 1:
                    arr = arr.reshape(-1, 1)
                arr = arr.astype(np.float32)
                cond_col = np.full((arr.shape[0], 1), float(cond_code), dtype=np.float32)
                block = np.hstack([arr, cond_col])
                blocks.append(block)
            except Exception:
                continue

        if not blocks:
            raise FileNotFoundError(
                f"No condition .txt files (other than Sync.txt) in {ev_dir} for subject {subject_id}"
            )
        ev = np.vstack(blocks)
        # Normalize: col0 onset, col1 duration -> divide by (177*0.72); col2 amplitude -> z-score; last col(s) unchanged
        time_scale = 177.0 * 0.72
        if ev.shape[1] >= 1:
            ev[:, 0] = ev[:, 0].astype(np.float64) / time_scale
        if ev.shape[1] >= 2:
            ev[:, 1] = ev[:, 1].astype(np.float64) / time_scale
        if ev.shape[1] >= 3:
            a = ev[:, 2].astype(np.float64)
            s = np.std(a)
            ev[:, 2] = (a - np.mean(a)) / (s if s > 1e-12 else 1.0)
        return ev.astype(np.float32)

    def load_subject(self, subject_id: str) -> np.ndarray:
        """
        Load resting state timeseries data for a specific subject.
        
        Args:
            subject_id: Subject ID string
            
        Returns:
            numpy array containing the resting state timeseries data
            
        Raises:
            FileNotFoundError: If the file doesn't exist for this subject
        """
        file_path = self.get_subject_path(subject_id)
        
        if not file_path.exists():
            raise FileNotFoundError(
                f"Resting state timeseries file not found for subject {subject_id}: {file_path}"
            )
        
        return np.load(file_path)
    
    def load_task_subject(self, subject_id: str) -> np.ndarray:
        """
        Load task timeseries data for a specific subject.
        
        Args:
            subject_id: Subject ID string
            
        Returns:
            numpy array containing the task timeseries data
            
        Raises:
            FileNotFoundError: If the file doesn't exist for this subject
        """
        if not self.task_name:
            raise ValueError("Task name not specified")
        
        file_path = self.get_task_path(subject_id)
        
        if not file_path.exists():
            raise FileNotFoundError(
                f"Task timeseries file not found for subject {subject_id}: {file_path}"
            )
        
        # Try loading as PyTorch first (common format for task data)
        if file_path.suffix in ['.pt', '.pth'] or file_path.suffix == '':
            try:
                task_data = torch.load(str(file_path), map_location='cpu')
                # Convert to numpy if it's a tensor
                if isinstance(task_data, torch.Tensor):
                    return task_data.numpy()
                elif isinstance(task_data, np.ndarray):
                    return task_data
                elif isinstance(task_data, dict):
                    # If it's a dict, try to extract the data
                    if 'data' in task_data:
                        data = task_data['data']
                        return data.numpy() if isinstance(data, torch.Tensor) else data
                    else:
                        # Take the first tensor/array value
                        for v in task_data.values():
                            if isinstance(v, (torch.Tensor, np.ndarray)):
                                return v.numpy() if isinstance(v, torch.Tensor) else v
                        raise ValueError(f"Could not extract array from dict: {list(task_data.keys())}")
                else:
                    raise ValueError(f"Unexpected data type: {type(task_data)}")
            except Exception as e:
                # If PyTorch loading fails, try NumPy
                try:
                    return np.load(str(file_path))
                except:
                    raise FileNotFoundError(
                        f"Could not load task data for subject {subject_id} from {file_path}: {e}"
                    )
        else:
            # Try NumPy loading
            return np.load(str(file_path))
    
    def load_all_subjects(self) -> Dict[str, np.ndarray]:
        """
        Load timeseries data for all subjects.
        
        Returns:
            Dictionary mapping subject IDs to their timeseries arrays
        """
        data = {}
        for subject_id in self.subject_ids:
            try:
                data[subject_id] = self.load_subject(subject_id)
            except FileNotFoundError as e:
                print(f"Warning: {e}")
                continue
        
        return data
    
    def __len__(self) -> int:
        """Return the number of subjects in the dataset."""
        return len(self.subject_ids)
    
    def __getitem__(self, idx: int) -> tuple:
        """
        Get a subject by index.
        
        Args:
            idx: Index of the subject
            
        Returns:
            If task_root is provided: Tuple of (subject_id, rest_timeseries, task_timeseries)
            Otherwise: Tuple of (subject_id, rest_timeseries)
        """
        subject_id = self.subject_ids[idx]
        rest_data = self.load_subject(subject_id)
        
        if self.task_name:
            try:
                task_data = self.load_task_subject(subject_id)
                return subject_id, rest_data, task_data
            except (FileNotFoundError, ValueError):
                return subject_id, rest_data
        else:
            return subject_id, rest_data


